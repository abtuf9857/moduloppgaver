package exam2018;


public class opgv1 {
    public static void main(String[]args){
        //while løkke som skriver ut alle tall mellom 0 og 25
        int i=0;
        while(i<25){
            i++;
            System.out.println(i+" ");
        }
        //for løkke som skriver ut alle tall mellom 0 og 25
        for(int j=0;j<=25;j++){
            System.out.println(j+" ");
        }

    }
}
